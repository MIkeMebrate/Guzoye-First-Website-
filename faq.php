            the system. You have entered an incorrect e-mail address. The email
            ticket has ended up in spam folder. Please check your bank statement
            first to see if your account/card was charged, please contact us in
            order to solve this problem. If your account was not charged, then
            you can retry the booking. If you have any further questions, please
            feel free to call our 24/7 customer support.
          </p>
          <h3>
            Is it mandatory to carry the required identity proofs along with the
            e-ticket?
          </h3>
          <p>
            Yes. It is mandatory to carry government issued identity proofs
            along with your e-ticket.
