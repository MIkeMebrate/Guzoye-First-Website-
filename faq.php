            data encryption. Using SSL ensures that the information exchanged
            with Guzoye.com is never transmitted unencrypted thus protecting the
            information from being viewed by unauthorized individuals. This
            encryption is done using 256-bit RC4 encryption. In addition the
            credit card information is processed over secure gateways which are
            certified by Visa and MasterCard.
          </p>
