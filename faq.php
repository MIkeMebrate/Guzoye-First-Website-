          <p>No. You cannot book your bus tickets by phone.</p>
          <h3>Is Home Delivery / Cash on Delivery of tickets available?</h3>
          <p>
            No. We do not have that facility for now. You can either carry the
            SMS confirmation we send or in some cases you can carry the ticket
            printout that you can print after booking confirmation.
          </p>
