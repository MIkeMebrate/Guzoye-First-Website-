          <h3>I missed the bus, am I eligible for a refund?</h3>
          <p>
            Guzoye provides 100% refund if the bus is missed due to either
            Guzoye or its partner company's fault. However, if the bus is missed
            due to any other reasons not directly related to Guzoye it does not
            provide any refund (for example: waiting at the wrong pickup point
            or arriving late at the pickup point is not considered for refund).
          </p>
          <h3>
            I have cancelled my booking and when can I get the refund amount?
          </h3>
          <p>
            Generally all refunds are processed the same day or instantly in
            most cases. Money is transferred/refunded back to the passenger's
            credit/debit card/Bank account by the payment gateway within 24
            hours. After which depending on the customer’s bank, it takes 2-7
            working days to reflect the credit in your account.
          </p>
          <h3>Would I get a full refund?</h3>
          <p>
            Full refund happens only when there has been an error from
            Guzoye.com’s end or the bus partners end. In other cases, where the
            traveler requests for cancellation, partial refund happens. Please
            check the cancellation terms of the particular bus partner - these
            are mentioned on the ticket printout/Email. Or please contact our
            customer support for more details.
          </p>
